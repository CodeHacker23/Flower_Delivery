package org.example.flower_delivery.util;

import org.example.flower_delivery.model.DeliveryInterval;

/**
 * Утилита для обрезки и форматирования текста под кнопки и сообщения Telegram.
 *
 * ЗАЧЕМ ВООБЩЕ ЭТОТ КЛАСС?
 * В Telegram у кнопок есть лимит на длину текста (порядка 64 байт). Если впихнуть
 * туда "Челябинск, ул. Труда, 72, подъезд 2, кв. 5" — кнопка либо обрежется криво,
 * либо часть текста не влезет. Поэтому мы сами режем строки до нужной длины и
 * убираем лишнее (город, квартира, подъезд), чтобы на кнопке было читаемо: "Труда 72".
 *
 * ОТКУДА ЭТО ВЗЯЛОСЬ?
 * Раньше эти методы сидели прямо в классе Bot — и Bot раздулся до 1500+ строк.
 * По принципу Single Responsibility (одна ответственность у класса) форматирование
 * текста — не дело бота. Бот должен только получать апдейты и отдавать их обработчикам.
 * Поэтому мы вынесли всё "порезать строку / склеить адрес" сюда.
 *
 * ПОЧЕМУ "Util" И СТАТИЧЕСКИЕ МЕТОДЫ?
 * Util = утилита, то есть набор функций без состояния. Нам не нужно хранить внутри
 * класса никаких полей (в отличие от, например, OrderService, который держит
 * репозитории). Мы просто подаём строку на вход — получаем строку на выходе.
 * Статические методы вызываются так: TextFormattingUtil.truncateForButton("хелло", 10)
 * без создания объекта new TextFormattingUtil().
 *
 * ПОЧЕМУ private конструктор?
 * Чтобы никто не сделал new TextFormattingUtil() — нам не нужны экземпляры,
 * все методы статические. Если кто-то попытается создать объект — компилятор/код
 * не даст (конструктор приватный). Аналогия: как калькулятор с кнопками "плюс",
 * "минус" — тебе не нужно "включать" калькулятор как объект, ты просто жмёшь кнопку.
 */
public final class TextFormattingUtil {

    /**
     * Приватный конструктор: экземпляры этого класса создавать нельзя.
     * См. выше — мы работаем только со статическими методами.
     */
    private TextFormattingUtil() {
    }

    // ========== ОБРЕЗКА СТРОКИ ДЛЯ КНОПКИ ==========

    /**
     * Обрезает строку до maxLen символов. Если длиннее — режем и в конец ставим точку.
     *
     * ЗАЧЕМ?
     * В Telegram текст кнопки не резиновый. Длинный адрес "ул. Труда, дом 72, корпус 1"
     * не влезет — получится уродство или ошибка. Поэтому мы сами режем до, например, 20
     * символов и ставим в конец "..." (у нас одна точка, чтобы сэкономить символ).
     *
     * ЧТО ЗНАЧИТ КАЖДОЕ СЛОВО:
     * - public    — метод можно вызвать из любого другого класса (не только внутри пакета).
     * - static    — метод принадлежит классу, а не объекту. Вызов: TextFormattingUtil.truncateForButton(...).
     * - String    — тип возвращаемого значения: метод возвращает строку (текст).
     * - truncate  — по-английски "обрезать". ForButton — "для кнопки". Итого: "обрезать для кнопки".
     * - (String s, int maxLen) — параметры: s это исходная строка, maxLen — максимальная длина.
     *
     * КАК РАБОТАЕТ ПО ШАГАМ:
     * 1. Если s == null или пустая — возвращаем "—" (тире), чтобы не показывать пустоту.
     * 2. s.trim() — убираем пробелы с начала и конца (чтобы "  Труда 72  " стало "Труда 72").
     * 3. Если длина уже <= maxLen — возвращаем как есть.
     * 4. Иначе берём подстроку с 0 по (maxLen - 1) и добавляем "." — чтобы было ровно maxLen символов
     *    и визуально понятно, что текст обрезан.
     *
     * ПРИМЕР:
     *   truncateForButton("Челябинск, ул. Труда, 72", 20)  →  "Челябинск, ул. Труда."
     *
     * ЕСЛИ НАПИСАТЬ ПО-ДРУГОМУ:
     * - Не проверять null — при s == null будет NullPointerException, бот упадёт.
     * - Резать без точки — пользователь не поймёт, что текст обрезан.
     */
    public static String truncateForButton(String s, int maxLen) {
        if (s == null || s.isEmpty()) return "—";
        s = s.trim();
        if (s.length() <= maxLen) return s;
        return s.substring(0, maxLen - 1) + ".";
    }

    // ========== АДРЕС: ТОЛЬКО УЛИЦА И ДОМ ==========

    /**
     * Оставляет в адресе только "улица + дом", без города, "ул.", подъезда, квартиры.
     *
     * ЗАЧЕМ?
     * В кнопках места мало. "Челябинск, ул. Труда, 72, подъезд 2, кв. 5" — перебор.
     * Курьеру на кнопке достаточно "Труда 72" — он и так в городе, остальное в деталях заказа.
     *
     * КАК РАБОТАЕТ (упрощённо):
     * 1. Ищем слова-маркеры: "подъезд", "кв.", "квартира", "домофон" и т.д.
     * 2. Всё что после первого такого маркера — отрезаем (подъезд и квартира не для кнопки).
     * 3. Убираем префикс города "Челябинск, " и "ул.", "пр." и т.п.
     * 4. Заменяем запятые на пробелы, схлопываем пробелы — получаем компактную строку.
     *
     * ПРИМЕР:
     *   "Челябинск, ул. Труда, 72, кв. 5"  →  "Труда 72"
     *
     * replaceFirst("(?i)^челябинск,?\\s*", "") — что за дичь?
     * - (?i)     — регистронезависимый поиск (и "Челябинск", и "челябинск").
     * - ^        — с начала строки.
     * - челябинск — буквально это слово.
     * - ,?       — запятая ноль или один раз.
     * - \\s*     — пробелы в любом количестве.
     * Итог: вырезаем "Челябинск, " или "челябинск " с начала.
     */
    public static String streetAndHouseOnly(String address) {
        if (address == null || address.isEmpty()) return "—";
        String s = address.trim();
        int cut = -1;
        String[] markers = {"подъезд", "подик", "под.", " кв.", " кв ", "квартира", "кв.", "домофон", " д."};
        String lower = s.toLowerCase();
        for (String m : markers) {
            int idx = lower.indexOf(m);
            if (idx > 0 && (cut < 0 || idx < cut)) cut = idx;
        }
        if (cut > 0) s = s.substring(0, cut).trim();
        if (s.endsWith(",")) s = s.substring(0, s.length() - 1).trim();
        s = s.replaceFirst("(?i)^челябинск,?\\s*", "");
        s = s.replaceFirst("(?i)^ул\\.?\\s*", "");
        s = s.replaceFirst("(?i)^пр\\.?\\s*", "");
        s = s.replaceFirst("(?i)^проспект\\s+", "");
        s = s.replace(", ", " ").replace(",", " ").replaceAll("\\s+", " ").trim();
        return s.isEmpty() ? "—" : s;
    }

    // ========== КОРОТКОЕ ВРЕМЯ ДЛЯ КНОПКИ (ИНТЕРВАЛ ДОСТАВКИ) ==========

    /**
     * Форматирует интервал доставки в короткий вид для кнопки: ASAP → "Срочно", 09:00 — 12:00 → "09-12".
     *
     * ЗАЧЕМ?
     * На кнопке "09:00 — 12:00" жрёт много места. "09-12" — компактно и понятно.
     * ASAP отдельно показываем как "Срочно", чтобы курьер сразу видел приоритет.
     *
     * interval.getTimeRange() — у enum DeliveryInterval есть поле timeRange, например "09:00 — 12:00".
     * replace(" — ", "-") — длинное тире с пробелами заменяем на дефис: "09:00-12:00".
     * replace(":00", "") — убираем ":00" чтобы было "09-12" (без минут).
     */
    public static String shortTimeForButton(DeliveryInterval interval) {
        if (interval == null) return "—";
        if (interval == DeliveryInterval.ASAP) return "Срочно";
        String range = interval.getTimeRange().replace(" — ", "-");
        return range.replace(":00", "").replace(":30", "");
    }

    // ========== КОРОТКИЙ АДРЕС ДЛЯ ПОДПИСИ КНОПКИ (ДО ПЕРВОЙ ЗАПЯТОЙ) ==========

    /**
     * Берёт часть адреса до первой запятой, не более 16 символов, по границе слова.
     *
     * ОТЛИЧИЕ ОТ streetAndHouseOnly:
     * - streetAndHouseOnly — выкидывает город, ул., кв., подъезд и т.д., оставляет "Труда 72".
     * - shortAddressForButton — просто "до первой запятой, макс 16 символов". Нужно для подписи
     *   кнопок типа "1. Труда 72 → В магазине", где лимит Telegram ~64 байта на всю кнопку.
     *
     * lastIndexOf(' ') — зачем? Если обрезали на 16-м символе посередине слова "Тухачевского",
     * получится "Тухачевского" (12 букв) — ок. Если получилось "Челябинск, ул." и мы режем до 16,
     * получим "Челябинск, ул. Т" — буква "Т" одна висит. Поэтому режем по последнему пробелу:
     * "Челябинск, ул." (без обрезанного слова).
     */
    public static String shortAddressForButton(String fullAddress) {
        if (fullAddress == null || fullAddress.isEmpty()) return "";
        String s = fullAddress.trim();
        int comma = s.indexOf(',');
        if (comma > 0) s = s.substring(0, comma).trim();
        final int max = 16;
        if (s.length() <= max) return s;
        s = s.substring(0, max);
        int lastSpace = s.lastIndexOf(' ');
        return lastSpace > 0 ? s.substring(0, lastSpace).trim() : s;
    }

    // ========== ЭКРАНИРОВАНИЕ MARKDOWN ==========

    /**
     * Экранирует символы _, *, [ для Telegram Markdown, чтобы они не ломали разметку.
     *
     * ЗАЧЕМ?
     * В Telegram при parseMode("Markdown") символы * делают жирный, _ — курсив, [ — ссылку.
     * Если в имени курьера или адресе есть "Иван_Петров" или "ул. Труда*", бот попытается
     * применить разметку и сообщение может отобразиться криво или упасть. Поэтому все
     * пользовательские строки, которые вставляем в текст с Markdown, прогоняем через escapeMarkdown.
     *
     * replace("_", "\\_") — обратный слэш перед подчёркиванием говорит Telegram: "это не разметка,
     * покажи символ как есть". Аналогично для * и [.
     *
     * ЧТО БУДЕТ ЕСЛИ НЕ ЭКРАНИРОВАТЬ?
     * Текст "Курьер *Иван* уехал" покажет "Иван" жирным. Если курьера зовут "ООО *Ромашка*",
     * получится "ООО Ромашка" (со звёздочками как разметка), а не "ООО *Ромашка*" как текст.
     */
    public static String escapeMarkdown(String s) {
        if (s == null) return "";
        return s.replace("_", "\\_").replace("*", "\\*").replace("[", "\\[");
    }
}
