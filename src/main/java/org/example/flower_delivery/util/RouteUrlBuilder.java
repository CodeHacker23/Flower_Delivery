package org.example.flower_delivery.util;

/**
 * Строит ссылки на маршруты в Яндекс.Картах и 2ГИС по координатам "откуда → куда".
 *
 * ЗАЧЕМ ЭТО НУЖНО?
 * Курьеру после "Вручил" или "Вернул в магазин" мы шлём сообщение с кнопками:
 * "Построить маршрут в Яндекс.Картах" и "Построить маршрут в 2ГИС". При нажатии
 * открывается карта с уже подставленным маршрутом от текущей точки (или от точки
 * курьера) до магазина/до точки доставки. Курьер не копирует адреса руками — тыкнул
 * и поехал.
 *
 * ОТКУДА ВЗЯЛОСЬ?
 * Раньше методы buildYandexRouteUrl и build2GisRouteUrl сидели в Bot. По SRP
 * бот не должен знать формат URL карт — это вынесли сюда. OrderBundleService тоже
 * строит ссылки на маршруты для связок заказов — можно будет переиспользовать эти
 * методы оттуда, чтобы не дублировать строки с URL.
 *
 * КООРДИНАТЫ: lat = широта (latitude), lon = долгота (longitude).
 * - fromLat, fromLon — откуда (например текущая позиция курьера).
 * - toLat, toLon     — куда (магазин или адрес доставки).
 *
 * Схема:
 *   [Курьер (fromLat, fromLon)]  ----маршрут---->  [Магазин (toLat, toLon)]
 */
public final class RouteUrlBuilder {

    private RouteUrlBuilder() {
    }

    /**
     * Ссылка на маршрут в Яндекс.Картах: от (fromLat, fromLon) до (toLat, toLon).
     *
     * КАК РАБОТАЕТ URL?
     * Яндекс.Карты принимают в параметре rtext две точки через тильду ~:
     *   rtext=широта1,долгота1~широта2,долгота2
     * rtt=auto — режим маршрута "на авто". Приложение/сайт Яндекса откроется с уже
     * построенным маршрутом между этими двумя точками.
     *
     * ПРИМЕР:
     *   buildYandexRouteUrl(55.16, 61.40, 55.17, 61.41)
     *   → "https://yandex.ru/maps/?rtext=55.16,61.4~55.17,61.41&rtt=auto"
     *
     * ЧТО БУДЕТ ЕСЛИ ПЕРЕПУТАТЬ lat и lon?
     * Маршрут построится в неправильном месте (например в океане). Для Челябинска
     * широта примерно 55, долгота примерно 61 — не перепутай.
     */
    public static String buildYandexRouteUrl(double fromLat, double fromLon, double toLat, double toLon) {
        return "https://yandex.ru/maps/?rtext=" + fromLat + "," + fromLon + "~" + toLat + "," + toLon + "&rtt=auto";
    }

    /**
     * Ссылка на маршрут в 2ГИС: от текущего местоположения пользователя до (toLat, toLon).
     *
     * ВАЖНО: в 2ГИС в формате routeSearch точка "А" (откуда) — это всегда текущая GPS
     * пользователя. В URL мы передаём только точку "Б" (куда). То есть формат 2ГИС
     * "построить маршрут ДО этой точки" (откуда — определит само приложение по GPS).
     *
     * Документация 2ГИС: to передаётся как lon,lat (сначала долгота, потом широта — да,
     * у них так). Поэтому в строке: toLon + "," + toLat.
     *
     * ПРИМЕР:
     *   build2GisRouteUrl(55.16, 61.40, 55.17, 61.41)
     *   → "https://2gis.ru/routeSearch/rsType/car/to/61.41,55.17"
     * Курьер откроет ссылку — 2ГИС возьмёт его GPS и построит маршрут до (55.17, 61.41).
     */
    public static String build2GisRouteUrl(double fromLat, double fromLon, double toLat, double toLon) {
        String to = toLon + "," + toLat;
        return "https://2gis.ru/routeSearch/rsType/car/to/" + to;
    }
}
