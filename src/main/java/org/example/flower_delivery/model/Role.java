package org.example.flower_delivery.model;

/**
 * Enum (перечисление) ролей пользователей.
 *
 * Роль определяет, что пользователь может делать в системе:
 * - COURIER: курьер, который бегает и доставляет заказы
 * - SHOP: магазин, который создает заказы и следит за доставкой
 * - ADMIN: админ, который рулит всем этим бардаком
 *
 * Enum используем вместо строк, чтобы:
 * - нельзя было опечататься ("COURIER" vs "COURIR")
 * - IDE подсказвала доступные значения
 * - код был типобезопасным (Role, а не просто String)
 */
public enum Role {

    /**
     * Роль: Курьер.
     * Курьер — исполнитель доставки:
     * - видит доступные заказы
     * - может брать заказы
     * - меняет статусы (В магазине, Забрал, В пути, Вручил)
     */
    COURIER,

    /**
     * Роль: Магазин.
     * Магазин:
     * - создает заказы
     * - видит статусы заказов
     * - может видеть, где курьер
     */
    SHOP,

    /**
     * Роль: Администратор.
     * Админ:
     * - активирует/блокирует курьеров и магазины
     * - управляет заказами
     * - настраивает комиссии, штрафы, депозиты
     */
    ADMIN
}

