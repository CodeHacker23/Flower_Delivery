package org.example.flower_delivery.model;

/**
 * Статус доставки отдельной точки в мультиадресном заказе.
 * 
 * Каждая точка может быть доставлена независимо от других.
 * Например: точка 1 доставлена, точка 2 ещё нет.
 */
public enum StopStatus {
    
    /**
     * Ожидает доставки.
     * Курьер ещё не доставил на эту точку.
     */
    PENDING,
    
    /**
     * Доставлено.
     * Курьер успешно доставил букет получателю.
     */
    DELIVERED,
    
    /**
     * Не удалось доставить.
     * Получатель не вышел, неверный адрес и т.д.
     */
    FAILED
}
