package org.example.flower_delivery.model;

/**
 * Состояния диалога регистрации курьера.
 *
 * Всё так же, как с магазином, только шагов три:
 * 1) Имя и фамилия
 * 2) Номер телефона (через кнопку "Поделиться номером")
 * 3) Фото (селфи с паспортом)
 */
public enum CourierRegistrationState {

    // Ожидаем ввод имени и фамилии курьера
    WAITING_FULL_NAME,

    // Ожидаем номер телефона (кнопка "Поделиться номером телефона")
    WAITING_PHONE,

    // Ожидаем фото (селфи с паспортом)
    WAITING_PASSPORT_PHOTO,

    // Не в процессе регистрации
    NONE
}

